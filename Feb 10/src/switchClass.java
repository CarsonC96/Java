
public class switchClass {

	public static void main(String[] args) {
		
		int status = 2;
		switch (status) {
		
		case 1: System.out.println("Status == " + status);
		break;
		case 2: System.out.println("Status == " + status);
		break;
		default: System.out.println("Invalid status code, please try again.");
		}

	}

}
